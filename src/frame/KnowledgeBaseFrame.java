    private void setDataToTable(List<Eat> eats) {
        DefaultTableModel df = (DefaultTableModel) jTable1.getModel();
        df.getDataVector().removeAllElements();
        df.fireTableDataChanged();
        for (Eat eat : eats) {
            List<String> foods = new ArrayList<>();
            for (Food food : eat.getFoods()) {
                foods.add(food.getFood());
            }
            Object[] row = new Object[]{
                eat.getId(),
                eat.getName(),
                foods.toString()
            };
            df.addRow(row);
        }
        df.fireTableDataChanged();
    }

    private void addDataToTable() {
        List<Eat> eats = DataProcesser.getEatBase();
        setDataToTable(eats);
    }

    private void addComboBoxToPanel() {
        JComboBox comboBox = new JComboBox();
        try {
            List<Food> foods = DataProcesser.getDefaultFoodList();
            for (Food f : foods) {
                comboBox.addItem(new ComboModel(f.getId(), f.getFood()));

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

        comboBox.addItemListener(new ItemChangedListener());

        panelHolder[xPos][yPos].add(comboBox);

        updateCoordinate();

        jPanel5.revalidate();
        jPanel5.repaint();
    }

    public void initAddingPanel() {
        int i = 5;
        int j = 5;
        panelHolder = new JPanel[i][j];
        jPanel5.setLayout(new GridLayout(i, j));
        for (int m = 0; m < i; m++) {
            for (int n = 0; n < j; n++) {
                panelHolder[m][n] = new JPanel();
                jPanel5.add(panelHolder[m][n]);
            }
        }
    }

