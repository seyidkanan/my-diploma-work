    class ItemChangedListener implements ItemListener {

        @Override
        public void itemStateChanged(ItemEvent event) {
            if (event.getStateChange() == ItemEvent.SELECTED) {
                ComboModel item = (ComboModel) event.getItem();
                foodIds.add(item.getId());
            }
        }

    }

